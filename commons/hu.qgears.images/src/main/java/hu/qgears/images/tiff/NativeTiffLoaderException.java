package hu.qgears.images.tiff;

import java.io.IOException;

public class NativeTiffLoaderException extends IOException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3645864110331291928L;

	public NativeTiffLoaderException() {
		super();
	}

	public NativeTiffLoaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public NativeTiffLoaderException(String message) {
		super(message);
	}

	public NativeTiffLoaderException(Throwable cause) {
		super(cause);
	}

	
	
}

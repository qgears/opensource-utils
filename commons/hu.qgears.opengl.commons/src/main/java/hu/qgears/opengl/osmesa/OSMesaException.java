package hu.qgears.opengl.osmesa;

public class OSMesaException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public OSMesaException() {
		super();
	}

	public OSMesaException(String message, Throwable cause) {
		super(message, cause);
	}

	public OSMesaException(String message) {
		super(message);
	}

	public OSMesaException(Throwable cause) {
		super(cause);
	}

}

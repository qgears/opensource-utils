package hu.qgears.opengl.glut;

public class GlutException extends Exception {
	private static final long serialVersionUID = 1L;

	public GlutException() {
		super();
	}

	public GlutException(String message, Throwable cause) {
		super(message, cause);
	}

	public GlutException(String message) {
		super(message);
	}

	public GlutException(Throwable cause) {
		super(cause);
	}
	
}
